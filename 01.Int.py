
# 정수형 (양&음의 정수 또는 0 대입)
a = 123
a = -178
a = 0
print(a)

# 실수형
a = 1.2
a = -3.45
a = 4.24E10 # 4.24x10^10
print(a)
a = 4.24e-10 # 4.24x10^-10
print(a)

# 8진수와 16진수 (파이썬에서는 잘 사용하지 않는 자료형)
# 8진수 표현 0o 또는 0O 작성 후 숫자
c = 0o177
print(c)

# 16진수 표현 0x 작성 후 숫자
d = 0x8ff
print(d)

# 사칙연산
e = 3
f = 4

print(e+f)
print(e-f)
print(e*f)
print(e/f)

# x의y제곱을 나타내는 **연산자
print(e**f)

# 나눗셈 후 나머지를 리턴하는 % 연산자
print(e%f)
print(f%e)
